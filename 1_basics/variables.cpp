#include <iostream>
using namespace std;

int main() {
    // Variable declaration
    int a = 5; // Integer variable
    float b = 3.14; // Float variable
    char c = 'A'; // Character variable
    string d = "Hello"; // String variable

    // Output the variables
    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Character: " << c << endl;
    cout << "String: " << d << endl;

    return 0;
}